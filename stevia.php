                  <h2>BUDIDAYA TANAMAN STEVIA</h2>
                  <div class="entry-meta">
                    <span class="author-meta"><i class="fa fa-user"></i> <a >Bertha Jessica</a></span>
                    <span><i class="fa fa-clock-o"></i> 4 Februari 2024</span>
                    <span class="tag-meta">
												<i class="fa fa-folder-o"></i>
												<a >budidaya</a>,
												<a >tanaman</a>
                                                <a >stevia</a>
                                                <a> teh tanpa gula </a>
											</span>
                   
                  <div class="entry-content">
                    <p>Tanaman stevia, yang juga dikenal sebagai Stevia rebaudiana, adalah sejenis tumbuhan yang berasal dari Amerika Selatan, terutama dari wilayah Paraguay dan Brasil. Tanaman ini terkenal karena daunnya yang memiliki rasa manis alami.

Stevia telah digunakan secara tradisional oleh suku asli di Amerika Selatan sebagai pemanis alami dalam makanan dan minuman. Daun stevia mengandung senyawa yang disebut steviol glikosida, yang memberikan rasa manis pada tanaman ini. Senyawa ini jauh lebih manis daripada gula, tetapi tidak memiliki kalori atau dampak negatif pada gula darah, sehingga beberapa orang menganggapnya sebagai pengganti gula yang sehat. </p>

<p> Untuk membudidayakan stevia dibutuhkan ketinggian 700 - 1.500 mdpl,
curah hujan 1.400 mm/tahun (116 mm/bulan) dengan suhu lingkungan
antara 20–24˚C, kelembaban tanah cukup tinggi, memiliki toleransi
tinggi terhadap tanah basah</p>

<p>Pembibitan dilakukan dengan stek pucuk batang dengan ciri memiliki 3-4
pasang daun dan tidak fase berbunga atau akan berbunga yang ditandai
dengan mengecilnya ukuran daun dan memendeknya ruas.</p>

<p> Stevia dapat ditanam di berbagai media, pada polybag Stevia dapat dipindahkan ke ukuran polybag
yang lebih besar untuk memudahkan
pertumbuhan akar yang lebih panjang dan lebih
kuat.
Pada setiap polybag dapat ditanami 1-2 bibit
stevia. Sedangkan pada lahan anda dapat menggunakan traktor maupun
menggunakan cangkul sedalam 25
cm. Pada saat pengolahan lahan
dapat ditambahkan pupuk kandang
untuk menyuburkan tanah sebelum
penanaman, dalam pembuatan bandengan panjang 5-10 meter dengan lebar 100-150 cm atau sesuai dengan kondisi
lahan
Penambahan mulsa plastik bertujuan untuk ·menekan pertumbuhan gulma
dan serangan penyakit, menjaga kelembaban tanah. Jarak tanam 25×25 cm atau 30×30 cm.
Pada setiap lubang tanaman diberi 250 gram pupuk
organik (pupuk kandang atau kompos).
Berhubung daun dan ranting bibit stevia cepat layu,
penanaman dilakukan pada pagi hari, sore hari, atau
saat mendung.</p>


                    <blockquote>
                      <p>
Untuk Pupuk, gunakan pupuk organik 5-10 ton/ha,
kemudian dengan pupuk urea
135 kg/ha (1,35 gram/lubang
tanam) atau Urea dan KCl
masing-masing sebanyak 2 g/
tanaman, serta diaplikasikan
pada 30 dan 60 HST.
</p>
                    </blockquote>
                    <p>Pada saat tanaman stevia berumur 2
minggu, sebaiknya setiap ujung tanaman
dipangkas untuk membentuk
percabangan sehingga produksi daun
akan lebih banyak.
Stevia membutuhkan banyak pasokan air
yang baik sepanjang tahun supaya
produksinya stabil</p>

<p>Stevia dapat terkena hama kutu maupun penyakit, untuk kutu ini biasanya menyerang pada
bagian pucuk tanaman dan terjadi
pada bulan kemarau.
Pencegahan dapat menggunakan
pestisida berbahan 10%
Imidakloprid (1: 1.000). Sedangkan penyakit biasanya terjadi pada tahap pembibitan,
mengakibatkan batang pertama menjadi
terganggu dan kemudian ada beberapa
titik kuning pada daun dan bintik bintik
akan memperbesar.
Pengendalian dapat dilakukan dengan
membuang tanaman yang terserang
serta didesinfeksi dengan bubuk kapur</p>

<p> Daun stevia dipanen pada 60 HST, cirinya yaitu
terlihat 5-10% dari total populasi tanaman mulai
membentuk bunga
Panen dapat dilihat dari tanaman memiliki tinggi
40 – 60 cm dengan pertumbuhan daun yang
rimbun.
Pada ketinggian seperti ini tanaman sudah mulai
memasuki masa berbunga dan pada saat ini pula
kandungan gula tanaman sedang berada pada
tingkat yang tertinggi.
Panen dilakukan saat pagi hari dengan cara
memotong 10-15 cm batang dari permukaan tanah.
</p>

<p>Pascapanen
dilakukan dengan merompes batang atau
ranting dan yang diambil hanya daundaunnya saja
Pengeringan yang dilakukan dengan sinar
matahari, daun diletakkan di atas alas, bila
cuacanya baik membutuhkan waktu 1-2 hari.
Pengeringan dengan menggunakan oven
membutuhkan waktu sekitar 4 jam pada
suhu 70 ºC.
Ciri daun stevia yang telah kering warnanya
hijau kekuningan. Daun-daun stevia yang telah
dikeringkan selanjutnya dikemas
dengan karung. Dengan cara
pengemasan yang baik dan tertutup
rapat, daun stevia bisa disimpan
sampai satu tahun bahkan lebih.</p>
