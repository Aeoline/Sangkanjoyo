                    <p>Kegiatan ini telah dilaksanakan pada Minggu, 28 Januari 2024 di desa Sangkanjoyo RT 04 dan Minggu, 4 Februari 2024 di balai desa yang dihadiri oleh perangkat desa, kepala dusun, dan perwakilan masyarakat.</p>
                    <p>Kegiatan ini dilatarbelakangi oleh proses pengelolaan sampah di Desa Sangkanjoyo yang belum optimal serta tidak tersedianya tempat pembuangan sampah sementara di lingkungan sekitar Desa Sangkanjoyo. Warga Desa Sangkanjoyo melakukan pengolahan sampah dengan cara membakar dan menimbun sampah. Cara ini dinilai belum tepat dan tidak menyelesaikan persoalan mengenai pengelolaan sampah dengan maksimal, justru menimbulkan masalah baru lainnya. Dampak yang ditimbulkan dari pembakaran sampah yaitu tercemarnya lingkungan, baik tanah, air, maupun udara, serta membahayakan kesehatan manusia.</p>
                    <p>Pengelolaan sampah di Desa Sangkanjoyo bertujuan untuk meminimalisir dampak dari pembakaran sampah yang dilakukan. Ada berbagai cara untuk mengelola sampah, diantaranya dengan lubang biopori dan ecobrick. Lubang biopori atau lubang resapan sebagai upaya untuk mengelola sampah organik, misalnya limbah rumah tangga, daun-daun kering, dan sisa makanan. Manfaat dari lubang biopori dapat menjadi sumur resapan yang mencegah banjir, menyuburkan tanah, serta menghasilkan pupuk kompos untuk tanaman. Kemudian, ecobrick sebagai upaya untuk mengelola sampah anorganik, seperti plastik, botol atau kaleng, dan sampah yang sulit terurai lainnya. Manfaat dari ecobrick yaitu dapat mengurangi bahaya sampah plastik, sehingga dapat menghasilkan produk yang lebih mempunyai nilai jual. Masyarakat dapat menukar produk ecobrick dengan sembako atau uang oleh pihak bank yang telah bekerja sama dengan Desa Sangkanjoyo, biasanya dilakukan sebelum bulan ramadhan. </p>                    
