                  <div class="entry-content">
                    <p>Sanggar kebudayaan ini di dirikan oleh Yudi Seto, seorang pemuda asli Desa Sangkanjoyo yang bercita-cita ingin menjadi seorang dalang. Beliau mulai belajar membuat pola wayang sejak duduk di bangku kelas 2 SD dan menekuni hal tersebut hingga beliau mulai lihai di saat kelas 3 SMP.  Selain karena ingin melestarikan budaya dan ingin menjadi pelopor kesenian wayang di daerah Kajen dan sekitarnya, beliau juga mempunyai harapan dengan adanya sanggar ini dapat memajukan dan ingin menaikan nama Desa Sangkanjoyo. 
</p>
<p>Bukan hanya sanggar wayang kulit dan pedalangan, beliau juga aktif dalam membentuk majelis keagamaan. Pada Tahun 2013, beliau keluar dari pesantren karena terketuk pintu hatinya dan ingin memajukan Desa Sangkanjoyo. Lalu pada tahun 2017, beliau mendirikan sebuah majelis agama dan pada tahun Februari 2019,  beliau membentuk Sanggar “Yudi Seto."
</p>
                    <blockquote>
                      <p>
Icon dan kebanggaan dari Sanggar "Yudi Seto" adalah wayang Brotoseno, dengan harapan sanggar ini dapat mengadopsi sifat mantep (yakin/kuat) dan tidak punya rasa minder. 
</p>
                    </blockquote>
                    <p>Lalu bagaimanakah tanggapan masyarakat sekitar terhadap adanya Sanggar "Yudi Seto"?
Tanggapan masyarakat mayoritas mendukung karena hal yang dilakukan positif seperti mengaji dan pelestarian budaya daerah.
</p>
<p>
Adakah harapan Ki Yudi Seto terhadap Sanggar dan Majelis "Yudi Seto" untuk kedepannya? Harapan beliau adalah tidak hanya tentang belajar agama saja tapi bisa mempergunakan budaya sebagai media dakwah seperti pewayangan agar dampak yang diberikan bisa bertambah.
</p>
